public class Main {
    static void myMethod(String fname,int age){
        System.out.println("First name is :"+fname+" and age is "+age);
    }
    public static void main(String[] args) {
        myMethod("Nireeksh",20);
        myMethod("Sakshi",15);
    }
}
//output :
// First name is :Nireeksh and age is 20.
// First name is :Sakshi and age is 15.