public class Main {
    public static void main(String[] args) {
        int n=6;
        // while loop
        System.out.println("while loop");
        while(n>0){
            System.out.println(n);
            n--;
        }

        // do while 
        System.out.println("Do while");

        int x = 1;
        do{
            System.out.println(x);
            x--;
        }while(x>1);
    }
}
