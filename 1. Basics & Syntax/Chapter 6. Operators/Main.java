public class Main {
    public static void main(String[] args) {
        
        int x = 3, y = 2; //assignment op

        System.out.println(x+y); //arithmetic operator + - * %  x++ x--

        System.out.println(x+=20);//addition assignment op.. x=3+20 = 23

        System.out.println(x>y);//comparision op == != < > <= etc returns boolean ans

        System.out.println(y > 3 && y < 10);//logical op
        
    }
}
