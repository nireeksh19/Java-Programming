public class Main {
    public static void main(String[] args) {
        // Note: All Math methods are static.

        System.out.println(Math.sqrt(25)); //5

        System.out.println(Math.max(4, 99)); //99

        System.out.println(Math.abs(-3.144)); // absolute positive value : 3.144

        System.out.println(Math.random()); // Random btw 0 to 1 

        System.out.println((int)(Math.random()*101)); // Random btw 1 TO 100
    }
}
